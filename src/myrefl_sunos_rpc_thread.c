/*
 * Multithreaded HA Diagnostics server
 *
 * Requires two threads, one for servicing RPC's and one for Diags
 * itself.
 */

#include <stdlib.h>
#include <pthread.h>
#include "myrefl_unix_clients.h"
#include "myrefl_unix_rpc.h"

/*
 * Import RPC declarations from the autogenerated RPC srv file.
 */
void myrefl_remote_master_protocol_1(struct svc_req *rqstp, 
                                    register SVCXPRT *transp);

void myrefl_remote_slave_protocol_1(struct svc_req *rqstp, 
                                   register SVCXPRT *transp);

void *myrefl_rpc_thread (void *thread_context)
{
    myrefl_unix_info_t *myrefl_info = thread_context;

    if (myrefl_info->master) {
        /*
         * Register the RPC Server for being a master
         */
        if (!svc_create(myrefl_remote_master_protocol_1, 
                        MYREFL_REMOTE_MASTER_PROTOCOL, 
                        MYREFL_MASTER_PROTOCOL_V1, "tcp")) {
            fprintf(stderr, "Unable to register master RPC protocol\n");
            exit(1);
        }
    } else {
        /*
         * Register the RPC Server for being a slave using the RPC
         * instance provided in the myrefl_info.
         */
        if (!svc_create(myrefl_remote_slave_protocol_1, 
                        myrefl_info->rpc_instance, 
                        MYREFL_SLAVE_PROTOCOL_V1, "tcp")) {
            fprintf(stderr, "Unable to register slave RPC protocol 0x%x\n",
                    myrefl_info->rpc_instance);
            exit(1);
        }
    }

    /*
     * Block and handle the incoming RPC requests on this thread
     */
    svc_run();
    pthread_exit(NULL);
    return(NULL);
}
